<template>
  <nav aria-label="Page navigation example">
    <ul class="pagination justify-content-center">
      <li class="page-item" :class="{'disabled' : !pages.has_pre}"
        @click.prevent="updatePage(pages.current_page -1 )">
        <a class="page-link" href="#local" aria-label="Previous">
          <span aria-hidden="true">&laquo;</span>
        </a>
      </li>
      <li class="page-item" v-for="(page, index) in pages.total_pages" :key="index"
       :class="{ 'active' : page === pages.current_page}">
        <a class="page-link" href="#local" @click.prevent="updatePage(page)">{{page}}</a>
      </li>
      <li class="page-item" :class="{'disabled' : !pages.has_next}"
       @click.prevent="updatePage(pages.current_page +1 )">
        <a class="page-link" href="#local" aria-label="Next">
          <span aria-hidden="true">&raquo;</span>
        </a>
      </li>
    </ul>
  </nav>
</template>

<script>
// :pages="{ 頁碼資訊 }"
// @emitPages="更新頁面事件"
export default {
  data () {
    return {

    }
  },
  props: ['pages'],
  methods: {
    updatePage (page) {
      this.$emit('update-page', page)
    }
  }
}
</script>
